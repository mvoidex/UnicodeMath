CODE_RE = re.compile(r'\\u([\da-fA-F]{4})|\\U([\da-fA-F]{8})|\\U\+([\da-fA-F]{4,8})')


def uchr(s):
    return chr(s) if PyV3 else unichr(s)


def symbol_by_code(codestr):
    """
    Gets symbol by code string '\\uXXXX', '\\UXXXXXXXX' or '\\U+XXXX[XXXX]'
    """
    m = CODE_RE.search(codestr)
    if m and m.end() == len(codestr):
        u = int(m.group(1) or m.group(2) or m.group(3), base=16)
        if PyV3 or u < 0xFFFF:
            return uchr(u)
        else:
            a = u / 0x0400 + 0xd7c0
            b = (u & 0x03ff) + 0xdc00
            return unichr(a) + unichr(b)
    return None


def replace_codes(s):
    """
    Replaces '\\uXXXX', '\\UXXXXXXXX' and '\\U+XXXX[XXXX]' with corresponding symbols
    """
    if not s:
        return s
    m = CODE_RE.search(s)
    if not m:
        return s
    before = s[:m.start()]
    code = s[m.start():m.end()]
    after = s[m.end():]
    return before + symbol_by_code(code) + replace_codes(after)


def code_by_symbol(sym):
    """
    Get code string in format '\\uXXXX' or '\\UXXXXXXXX' by symbol
    """
    if len(sym) == 1:
        c = ord(sym[0])
        if c > 0xFFFF:
            return u'\\U%08X' % c
        else:
            return u'\\u%04X' % c
    if len(sym) == 2:
        a = ord(sym[0])
        b = ord(sym[1])
        u = (a - 0xd7c0) * 0x0400 + (b - 0xdc00)
        return u'\\U%08X' % u
    return None


def get_settings():
    return sublime.load_settings('UnicodeMath.sublime-settings')


def update_and_subscribe(d, initd, inverse_update, key):
    def refill():
        d.clear()
        d.update(initd())
        d.update(dict((k, replace_codes(v)) for k, v in get_settings().get(key, {}).items()))
        inverse_update()
    refill()
    get_settings().add_on_change(key, refill)


def update_inverse_maths():
    global inverse_maths
    inverse_maths = dict((v, k) for k, v in maths.items())


def update_inverse_synonyms():
    global inverse_synonyms
    inverse_synonyms = {}
    for k, v in synonyms.items():
        inverse_synonyms[v] = inverse_synonyms.get(v, [])
        inverse_synonyms[v].append(k)


def names_by_symbol(symbol):
    """
    Returns list of names by symbol specified, first element is name, others - synonyms
    If no symbol found, returns empty list
    """
    global inverse_maths
    global inverse_synonyms
    res = []
    name = inverse_maths.get(symbol, None)
    if not name:
        return res
    res.append(name)
    if name:
        res.extend(inverse_synonyms.get(name, []))
    return res


def symbol_by_name(name, exclude=None):
    """
    Returns symbol by name or synonym or None
    exclude is used to prevent loops in synonyms

    """
    if exclude is None:
        exclude = []
    global maths
    global synonyms
    if name in exclude:
        return None
    if name in maths:
        return maths[name]
    if name in synonyms:
        exclude.append(name)
        return symbol_by_name(synonyms[name], exclude)  # synonym may ref on other synonym
    return None


def plugin_loaded():
    global maths
    global synonyms
    update_and_subscribe(maths, make_maths, update_inverse_maths, 'symbols')
    update_and_subscribe(synonyms, make_synonyms, update_inverse_synonyms, 'synonyms')

if int(sublime.version()) < 3000:
    plugin_loaded()
